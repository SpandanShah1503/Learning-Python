"Here when we assign the value of a variable at that time the data type is set"

x = "Hello"
y = 5
z = {1,2,3}
a = [1,2,3]

print(type(x))
print(type(y))
print(type(z))
print(type(a))


"Now if you want to define the data type explicitly we use constructors "

a = int(5)
print(type(a))